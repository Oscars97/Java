
public class Operaciones {
    private int a;
    private int b;
    
    public Operaciones (int numero1, int numero2 ){
        this.a = numero1;
        this.b = numero2;
    }
    public int sumar (){
        return a+b;
    }
    
    public int restar (){
        return a-b;
    }
    public int multiplicar (){
        return a*b;
    }
}

